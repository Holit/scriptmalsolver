    def get_pid_random(self, start: int = 0x1000, end: int = 0xffff0000):
        '''
        获取一个随机的pid
        '''
        while True:
            rand_num = random.randint(start, end) 
            if rand_num not in self.used_pid:  
                self.used_pid.add(rand_num) 
                return rand_num 
            
    def add_sys_process(self,name:str,pid:int = -1):
        ''''
        向配置文件中增加系统进程信息
        '''
        if(pid == -1):
            pid = self.get_pid_random(100,0x1000)
            
        self.add_process(name,pid)

    def add_process(self, name: str, pid: int = -1):
        '''
        向配置文件中增加进程信息
        '''
        if(pid == -1):
            pid = self.get_pid_random()
        
        self.set_value('PROCESS',name,pid)
    
