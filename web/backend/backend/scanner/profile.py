    @staticmethod
    def get_supported_roofs():
        index = 0
        from .analyzer import Analyzer
        rootfs_dir = Analyzer.get_roofts_root_dir()
        windows_editions = os.listdir(rootfs_dir)
        # 在各个editions里寻找ntdll.dll
        import fnmatch
        dll_files = []
        rootfs = []
        for root, dirs, files in os.walk(rootfs_dir):
            for file in files:
                if fnmatch.fnmatch(file, 'ntdll.dll'):
                    dll_files.append(os.path.join(root, file))
        for dll_file in dll_files:
            target_rootfs = os.path.dirname(os.path.dirname(os.path.dirname(dll_file)))
            name = target_rootfs.replace(rootfs_dir, '').removeprefix('\\').replace('\\', '-')
            rootfs.append(
                {
                    'index' : index,
                    'name': name,
                    'path': target_rootfs
                }
            )
            index = index + 1
        return rootfs
